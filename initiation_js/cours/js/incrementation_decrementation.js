// incrementation_decrementation
// Déclarer une variable et affecter une valeur : num1 :5
// Incrémentation d'une variable :
var num1 = 5;

console.log(num1);
num1 = 5 + 1;

console.log(num1);
num1 = num1 + 1;

console.log(num1);
num1 += num1;

console.log(num1);
num1++;

console.log(num1);
++num1;

console.log(num1);
++num1;

// Décrémentation

num1 = num1 - 1;
console.log(num1);

num1 -= 1;
console.log(num1);

num1--;
console.log(num1);

--num1;
console.log("value : " + num1);
